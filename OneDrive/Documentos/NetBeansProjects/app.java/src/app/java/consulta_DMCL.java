    private void btnconsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnconsultarActionPerformed
        Connection con = null;
        try {
            con = getConection();
            ps = con.prepareStatement("SELECT * FROM pedidos1 WHERE Menu = ? ");
            ps.setString(1, cbxmenu.getSelectedItem().toString());

            res = ps.executeQuery();
            if (res.next()) {
                txtnombre.setText(res.getString("Nombre"));
                txtapellido.setText(res.getString("Apellido"));
                txtcorreo.setText(res.getString("Correo"));
                txtdirecion.setText(res.getString("Direccion"));
                cbxbarrio.setSelectedItem(res.getString("Barrio"));
                txtcelular.setText(res.getString("Celular"));
                cbxplatos.setSelectedItem(res.getString("platos"));
                txtobservaciones.setText(res.getString("observaciones"));

            } else {
                JOptionPane.showMessageDialog(null, "No existe el domicilio ");
            }

        } catch (Exception e) {
            System.err.println(e);
        }
    

    }//GEN-LAST:event_btnconsultarActionPerformed

